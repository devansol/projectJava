    private void btnCetakRapotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakRapotActionPerformed

        String nis = cmbNis.getSelectedItem().toString();
        String tahun_ajaran = cmbTahunAjaran.getSelectedItem().toString();
        try {
            if(nis.equals("--Pilih--")){
                JOptionPane.showMessageDialog(null, "Nis belum di pilih !");
                cmbNis.requestFocus();
                return;
            }else if(tahun_ajaran.equals("--Pilih--")){
                JOptionPane.showMessageDialog(null, "Tahun Ajaran belum di pilih !");
                cmbTahunAjaran.requestFocus();
                return;
            }else{
                loadCetakRapot(nis,tahun_ajaran);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormCetakRapot.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCetakRapotActionPerformed

