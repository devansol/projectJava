    private class ComboBoxListener implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent e) {
            if(cmbNis.getSelectedItem().equals("--Pilih--")){
                inpNamaSiswaReport.setText("");
            }else{
                Siswa siswa=(Siswa) cmbNis.getSelectedItem();
                inpNamaSiswaReport.setText(siswa.getNama_siswa());
            }
        }
        
    }
    
    private void loadSiswa() throws Exception{
        cmbNis.removeAllItems();
        List<Siswa> siswa = siswaService.getAllSiswa();
        cmbNis.addItem("--Pilih--");
        
        for(Siswa result : siswa){
            cmbNis.addItem(result);
        } 
    }
    
    private void tahunAjaran() throws Exception{
        int year = Calendar.getInstance().get(Calendar.YEAR);
        int years = year + 1;
        cmbTahunAjaran.removeAllItems();
        cmbTahunAjaran.addItem("--Pilih--");
        for(int i = 2010 ; i <= year; i++){
            for(int j = i ; j <= i; j++){
                int newYear = j+1;
                cmbTahunAjaran.addItem(i + "/" + newYear);
            }
        }
    }
    
    private void loadCetakRapot(String nis, String tahun_ajaran) throws Exception{
        try {
            String namaFile = "src"+File.separator+"report"+File.separator+"rapot.jasper";
            Connection conn = service.KoneksiDb.getConnection();

            Map<String,Object> map =  new HashMap<String, Object>();
            map.put("nis", nis);
            map.put("tahun_ajaran", tahun_ajaran);
            JasperPrint jprint = JasperFillManager.fillReport(namaFile.toString(), map, conn);
            if(jprint.getPages().size() != 0){
                JasperViewer.viewReport(jprint,false);
            }else{
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan !");
            }
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }

